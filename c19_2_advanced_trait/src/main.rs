use std::fmt;

trait OutlinePrint: fmt::Display {
    fn outline_print(&self) {
        let output = self.to_string();
        let len = output.len();
        println!("{}", "*".repeat(len + 4));
        println!("*{}*", " ".repeat(len + 2));
        println!("* {} *", output);
        println!("*{}*", " ".repeat(len + 2));
        println!("{}", "*".repeat(len + 4));
    }
}

struct Point {
    x: i32,
    y: i32,
}

impl fmt::Display for Point {
    fn fmt(&self, f: &mut fmt::Formatter) -> fmt::Result {
        writeln!(f, "({}, {})", self.x, self.y)
    }
}

impl OutlinePrint for Point {}

// Using the Newtype Pattern to Implement External Traits on External Types
// Newtype is a term that originates from the Haskell programming language. 
// There is no runtime performance penalty for using this pattern, 
// and the wrapper type is elided at compile time.
struct Wrapper(Vec<String>);

impl fmt::Display for Wrapper {
    fn fmt(&self, f: &mut fmt::Formatter) -> fmt::Result {
        write!(f, "[{}]", self.0.join(", "))
    }
}

fn main() {
    println!("Hello, world!");

    let mi = Millimeters(1000);
    let meter = Meters(1);
    println!("{:?}", mi + meter);

    let person = Human;
    //person.fly();
    Wizard::fly(&person);

    println!("A baby dog is called a {}", <Dog as Animal>::baby_name());

    let w = Wrapper(vec![String::from("hello"), String::from("world")]);
    println!("w = {}", w);
}


